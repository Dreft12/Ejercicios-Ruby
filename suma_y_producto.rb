class SumaYProducto
  puts "Ingrese primer numero"
  num_1 = gets.to_i
  puts "Ingrese segundo numero"
  num_2 = gets.to_i
  puts "Ingrese tercer numero"
  num_3 = gets.to_i
  puts "Ingrese cuarto numero"
  num_4 = gets.to_i
  suma = num_1 + num_2
  prod = num_3 * num_4
  puts "La suma de los dos primeros numeros es de "
  puts suma
  puts "El producto de los dos ultimos numeros es de "
  puts prod
end